package lando.nsf.apu.triangle;

public class TriangleSequencer {

    private static final int[] SEQUENCE = {
            0xF, 0xE, 0xD, 0xC,
            0xB, 0xA, 0x9, 0x8,
            
            0x7, 0x6, 0x5, 0x4,
            0x3, 0x2, 0x1, 0x0,
            
            0x0, 0x1, 0x2, 0x3,
            0x4, 0x5, 0x6, 0x7,
            
            0x8, 0x9, 0xA, 0xB,
            0xC, 0xD, 0xE, 0xF};
    
    private int step = 0;
    
    void clock() {
        ++step;
    }
    
    public int getOutput() {
        return SEQUENCE[step & 31];
    }
}
